//Арифметик операторууд

console.log("Arithmetic Operators");
let numOne = 10;
let numTwo = 5;

let sum = numOne + numTwo; //нэмэх (+)
let diff = numOne - numTwo; //хасах (-)
let mult = numOne * numTwo; //үржих (*)
let div = numOne / numTwo; //хуваах (/)
let remainder = numOne % numTwo; //үлдэгдэлгүй хуваах (%)
let powerOf = numOne ** numTwo; //зэргээр үржүүлэх

console.log(
  sum +
    "\n" +
    diff +
    "\n" +
    mult +
    "\n" +
    div +
    "\n" +
    remainder +
    "\n" +
    powerOf
);
